public class incrementdecrement {
    public static void main(String[] args) {
        int apple=0;
        int price=10;
        System.out.println(apple++);//print non increment then increment the value
        System.out.println(++apple);//now apple=1 after pre increment apple =2
        System.out.println(price--);//post decrement
        System.out.println(--price);//pre decrement
        
    }
}
